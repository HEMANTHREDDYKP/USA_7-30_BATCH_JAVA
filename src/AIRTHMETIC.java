
public class AIRTHMETIC {

	public static void main(String[] args) {

		int a = 5;
		int b = 70;

		int res = a + b; // ---> 75
		int res1 = a - b; // ---> -65
		int res2 = b % a; // ---> 0
		int res3 = b / a; // --- 14
		int res4 = a * b;

		System.out.println(res);
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);

	}

}
