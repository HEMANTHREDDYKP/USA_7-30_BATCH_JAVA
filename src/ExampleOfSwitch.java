
public class ExampleOfSwitch {

	
	
	public static void main(String[] args) {
		
		//no duplicate cases
		//it can contain n nnumber of cases
		
		int a=10;
		switch(a) {
		
		case 1:
			System.out.println("Hi");
		    break;
		case 2:
			System.out.println("Hello");
			break;	
		default:
			System.out.println("Bye");
		}
	}
}
