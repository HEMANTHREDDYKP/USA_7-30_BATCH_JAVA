
public class ExampleForAnArray {

	
	
	
public static void main(String[] args) {
	
	
	int[] ages=new int[4];
	
	ages[0]=34;
	
	System.out.println(ages[0]);//34
	System.out.println(ages[1]);//0
	System.out.println(ages[2]);//0
	System.out.println(ages[3]);//0
	System.out.println("-------------------");
	ages[3]=8;
	System.out.println(ages[0]);//34
	System.out.println(ages[1]);//0
	System.out.println(ages[2]);//0
	System.out.println(ages[3]);//8
	
}
	
}
